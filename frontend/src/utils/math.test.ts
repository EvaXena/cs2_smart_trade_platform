import { describe, it, expect } from 'vitest'

describe('Math utilities', () => {
  it('should add numbers correctly', () => {
    expect(1 + 1).toBe(2)
  })

  it('should subtract numbers correctly', () => {
    expect(5 - 3).toBe(2)
  })

  it('should multiply numbers correctly', () => {
    expect(3 * 4).toBe(12)
  })
})
